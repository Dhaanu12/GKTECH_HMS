const { pool } = require('../config/db');

async function checkDependencies() {
    try {
        console.log('Checking dependencies for "patients" table...');
        const queryText = `
      SELECT
          tc.table_name, 
          kcu.column_name,
          tc.constraint_name
      FROM 
          information_schema.table_constraints AS tc 
          JOIN information_schema.key_column_usage AS kcu
            ON tc.constraint_name = kcu.constraint_name
            AND tc.table_schema = kcu.table_schema
          JOIN information_schema.constraint_column_usage AS ccu
            ON ccu.constraint_name = tc.constraint_name
            AND ccu.table_schema = tc.table_schema
      WHERE tc.constraint_type = 'FOREIGN KEY' AND ccu.table_name='patients';
    `;

        const res = await pool.query(queryText);
        console.log('Tables referencing "patients":');
        res.rows.forEach(row => {
            console.log(`- ${row.table_name} (${row.column_name}) [${row.constraint_name}]`);
        });

    } catch (err) {
        console.error('‚ùå Error checking dependencies:', err);
    } finally {
        pool.end();
    }
}

checkDependencies();
